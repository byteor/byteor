/*
 * BYTE ORDER
 *
 *  00000000 00000000 00000100 00000011
 *
 *  --------------------------------------------
 *  |  address  |  big-endian  | little-endian |
 *  --------------------------------------------
 *  |     0     |   00000000   |    00000011   |
 *  --------------------------------------------
 *  |     1     |   00000000   |    00000100   |
 *  --------------------------------------------
 *  |     2     |   00000100   |    00000000   |
 *  --------------------------------------------
 *  |     3     |   00000011   |    00000000   |
 *  --------------------------------------------
 */
#include <stdio.h>

int main(void)
{
    int x = 1;

    if (*(char *)&x == 1)
        printf("little-endian\n");
    else
        printf("big-endian\n");

    return 0;
}
